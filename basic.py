# Importing the required modeules
import platform as plfm

# printing the Architecture of the OS 
print("[+] Architecture :",plfm.architecture())
# displaying the machine
print("[+] Machine :", plfm.machine())
# printing the OS release information
print("[+] Operating System Release :",plfm.release())
# printing the currently using system name
print("[+] System Name :", plfm.system())
# printing the version of OS
print("[+] Operating System version :",plfm.version())
# printing the node or hostname of your OS
print("[+] Hostname :",plfm.node())
# printing the system platform
print("[+] Platform :",plfm.platform())
# printing processor information
print("[+] Processor :",plfm.processor())
